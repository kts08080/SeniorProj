// Transfers the local buffer to the CGRAM in the SSD1306
void transferBuffer(int *p, int size,unsigned char xCol,unsigned char yPage)
{
	int j=0;
      // set the Column and Page addresses to 0,0
      setColAddress(xCol);
      setPageAddress(yPage);

      for(j=0;j<size;j++)
      {

        I2C2WriteRegister(OLED_addr, 0x40, *p); // Writes 0x00 to data config register (0x0E).
        p++;
      }

}

