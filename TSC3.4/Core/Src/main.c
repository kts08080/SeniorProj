void PrintChar_OLED(char ascii_x)

/*  This function will transfer the shape of each ascii character to the display.  Each character
 * is a matrix of points 5x7 dots,  The pattern for each character is given in the global array
 * asciiChar[].  Ex. the pattern for a '0' =ASCII 48 (0x30) is the 48th row of 5 values in the array
 *
 * 	0x3E, 0x51, 0x49, 0x45, 0x3E,  map these binary values vertically and you get an ASCII '0'
 *   -     x     x     x     -
 *   x     -     -     -     x
 *   x     x     -     -     x
 *   x     -     x     -     x
 *   x     -     -     x     x
 *   x     -     -     -     x
 *   -     x     x     x     -
 *     */
{
	int j,calc_Array_Index;
      calc_Array_Index= 5 * ascii_x; //find row in ascii array
      I2C2WriteRegister(OLED_addr, 0x40, 0); //some space before each character
      for(j=0;j<5;j++)
      {
        I2C2WriteRegister(OLED_addr, 0x40, asciiChar[calc_Array_Index]);
        calc_Array_Index++;
      }
      I2C2WriteRegister(OLED_addr, 0x40, 0); //some space after each character
}

void WriteString_OLED(char *str)
{	while (*str)
	{
	  PrintChar_OLED(*str);
	  str++;
	}
}
