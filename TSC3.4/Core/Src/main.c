void I2C2WriteRegister(unsigned char Recv_addr, char Reg_addr, char Data) {
	//set up slave address (shifted 1 spot) and 2 byte transmission
	I2C2->CR2 = I2C_CR2_AUTOEND | (2 << 16) | Recv_addr << 1; //I2C_CR2_RD_WRN = 0 for write transfer; 7 bit address
	I2C2->CR2 |= I2C_CR2_START;   // start transfer

	while ((I2C2->ISR & I2C_ISR_TXIS) == 0)
		;
	I2C2->TXDR = Reg_addr;
	while ((I2C2->ISR & I2C_ISR_TXIS) == 0)
		;
	I2C2->TXDR = Data;
//	while ((I2C2->ISR &I2C_ISR_TC) ==0);

}
